        String mob=jTextField2.getText();
        String sub=(String) jComboBox1.getSelectedItem();
        String emailid=jTextField3.getText();
        String  emid=jTextField4.getText();

        char ch[]=jPasswordField1.getPassword();
        String ps=String.valueOf(ch);

        if(name.isEmpty())
        {
            JOptionPane.showMessageDialog(null," name no is Empty");
        }
        else if(!isvaildmobile(mob) )
        {
            JOptionPane.showMessageDialog(null,"  mobile no is invaild ");
        }
        else if(!isvaildmail(emailid) )
        {
            JOptionPane.showMessageDialog(null,"  email Id is invaild ");
        }
 else if(sub.isEmpty() )
     {
        JOptionPane.showMessageDialog(null,"  subject is empty ");
     }
        else if(emid.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Emp_ID is empty");
        }
        else if(ps.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"password is empty");
        }
        else
        {

            staffupdatemaker i=new staffupdatemaker();
            if(i.updatedata(name, mob, emailid, sub, emid, ps))
            {
                JOptionPane.showMessageDialog(null,"Staff  updated ");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Please try again");

            }

        }

